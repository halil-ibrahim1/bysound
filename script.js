/* ========================================== BÖLÜM: İLETİŞİM FORMU (Fetch API) ========================================== */
function cleanValue(value) {
  return value.replace(/[<>]/g, "").trim();
}

function validateForm() {
  if (!fullName || !contactInfo || !message || !submitBtn) return;
  const nameOk = cleanValue(fullName.value).length >= 2;
  const contactOk = cleanValue(contactInfo.value).length >= 5;
  const messageOk = cleanValue(message.value).length >= 3;
  submitBtn.disabled = !(nameOk && contactOk && messageOk);
}

[fullName, contactInfo, message].forEach((input) => {
  input?.addEventListener("input", () => {
    if (input === message) messageEdited = true;
    validateForm();
  });
});

contactForm?.addEventListener("submit", async (event) => {
  event.preventDefault();
  validateForm();
  if (submitBtn?.disabled) {
    formStatus.textContent = t("form.validation");
    formStatus.classList.add("error");
    return;
  }

  const payload = {
    fullName: cleanValue(fullName.value).slice(0, 80),
    contactInfo: cleanValue(contactInfo.value).slice(0, 120),
    message: cleanValue(message.value).slice(0, 1200),
    website: honeypot?.value || "",
    lang: currentLang
  };

  formStatus.textContent = t("form.sending");
  formStatus.classList.remove("error");
  submitBtn.disabled = true;

  try {
    const response = await fetch("contact.php", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(payload)
    });

    if (!response.ok) throw new Error("Request failed");

    formStatus.textContent = t("form.success");
    contactForm.reset();
    message.value = "";
    messageEdited = false;
  } catch (error) {
    formStatus.textContent = t("form.error");
    formStatus.classList.add("error");
  } finally {
    validateForm();
  }
});

