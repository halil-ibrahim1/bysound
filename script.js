function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem("bysound-lang", lang);
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    element.textContent = translations[lang][key] || key;
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const key = element.getAttribute("data-i18n-html");
    element.innerHTML = translations[lang][key] || key;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    element.setAttribute("placeholder", translations[lang][key] || "");
  });

  langButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  const nextDefault = translations[lang]["form.defaultMessage"];
  if (message && (!messageEdited || message.value.trim() === previousDefaultMessage || message.value === "")) {
    
    // Zorla katı metin basma huyundan vazgeçirip hayalet yazıyı atıyoruz:
    message.setAttribute("placeholder", nextDefault);
    
    // Hayalet yazının görünmesi için kutunun içini tamamen temizliyoruz:
    message.value = ""; 
    
    messageEdited = false;
  }
  previousDefaultMessage = nextDefault;
  validateForm();
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

