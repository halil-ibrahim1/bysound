/* ========================================== BÖLÜM: IŞIK ROBOTLARI ANİMASYONU ========================================== */
const robotStates = lightRobots.map((robot) => ({
  robot,
  axis: robot.querySelector(".robot-axis"),
  currentAngle: 105,
  targetAngle: 105
}));

let pointer = {
  x: window.innerWidth / 2,
  y: 120
};

function shortestAngle(from, to) {
  return ((to - from + 540) % 360) - 180;
}

function updateRobotTargets() {
  robotStates.forEach((state) => {
    if (!state.axis) return;
    const rect = state.axis.getBoundingClientRect();
    const centerX = rect.left + rect.width / 2;
    const centerY = rect.top + rect.height / 2;
    state.targetAngle = Math.atan2(pointer.y - centerY, pointer.x - centerX) * 180 / Math.PI;
  });
}

function animateRobots() {
  robotStates.forEach((state) => {
    if (!state.axis) return;
    const diff = shortestAngle(state.currentAngle, state.targetAngle);
    state.currentAngle += diff * 0.15;
    state.axis.style.setProperty("--robot-angle", `${state.currentAngle}deg`);
  });
  requestAnimationFrame(animateRobots);
}

document.addEventListener(
  "mousemove",
  (event) => {
    pointer = { x: event.clientX, y: event.clientY };
    updateRobotTargets();
  },
  { passive: true }
);

document.addEventListener(
  "touchmove",
  (event) => {
    const touch = event.touches[0];
    if (!touch) return;
    pointer = { x: touch.clientX, y: touch.clientY };
    updateRobotTargets();
  },
  { passive: true }
);

window.addEventListener("resize", updateRobotTargets);
window.addEventListener("load", () => {
  setLanguage(currentLang);
  updateRobotTargets();
  animateRobots();
  if (window.location.hash) {
    setTimeout(() => scrollToSection(window.location.hash, window.location.hash === "#hero"), 80);
  }
});

