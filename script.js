function getFixedRobotOffset() {
  return lightRigBar?.offsetHeight || 0;
}

function scrollToSection(hash, forceTop = false) {
  const target = document.querySelector(hash);
  if (!target) return;

  if (forceTop || hash === "#hero" || hash === "#top") {
    window.scrollTo({ top: 0, behavior: "smooth" });
    return;
  }

  const rect = target.getBoundingClientRect();
  const absoluteTop = window.scrollY + rect.top;
  const fixedOffset = getFixedRobotOffset();
  const availableHeight = Math.max(window.innerHeight - fixedOffset, 1);
  const targetCenter = absoluteTop + rect.height / 2;
  const viewportCenter = fixedOffset + availableHeight / 2;

  window.scrollTo({
    top: Math.max(0, targetCenter - viewportCenter),
    behavior: "smooth"
  });
}

